package com.br.globalsolution.classes;

public class ListaInventario {
	private String freezer;
	private String geladeira;
	private String despensa;
	public String getFreezer() {
		return freezer;
	}
	public void setFreezer(String freezer) {
		this.freezer = freezer;
	}
	public String getGeladeira() {
		return geladeira;
	}
	public void setGeladeira(String geladeira) {
		this.geladeira = geladeira;
	}
	public String getDespensa() {
		return despensa;
	}
	public void setDespensa(String despensa) {
		this.despensa = despensa;
	}
}
